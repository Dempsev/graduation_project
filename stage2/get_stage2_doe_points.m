function doeTable = get_stage2_doe_points(cfg)
%GET_STAGE2_DOE_POINTS Build the fixed 15-point local DOE for stage-2.
% The DOE perturbs only a1 / a2 / b2 / r0 around the trusted mother.

rows = [ ...
    make_row('pt01_center',       0.45,  0.00,  0.00, 0.012); ...
    make_row('pt02_a1_lo',        0.35,  0.00,  0.00, 0.012); ...
    make_row('pt03_a1_hi',        0.55,  0.00,  0.00, 0.012); ...
    make_row('pt04_a2_lo',        0.45, -0.08,  0.00, 0.012); ...
    make_row('pt05_a2_hi',        0.45,  0.08,  0.00, 0.012); ...
    make_row('pt06_b2_lo',        0.45,  0.00, -0.08, 0.012); ...
    make_row('pt07_b2_hi',        0.45,  0.00,  0.08, 0.012); ...
    make_row('pt08_r0_lo',        0.45,  0.00,  0.00, 0.011); ...
    make_row('pt09_r0_hi',        0.45,  0.00,  0.00, 0.013); ...
    make_row('pt10_a1hi_a2hi',    0.55,  0.08,  0.00, 0.012); ...
    make_row('pt11_a1hi_b2lo',    0.55,  0.00, -0.08, 0.012); ...
    make_row('pt12_a1lo_a2lo',    0.35, -0.08,  0.00, 0.012); ...
    make_row('pt13_a1lo_b2hi',    0.35,  0.00,  0.08, 0.012); ...
    make_row('pt14_a2hi_b2lo',    0.45,  0.08, -0.08, 0.012); ...
    make_row('pt15_a2lo_b2hi',    0.45, -0.08,  0.08, 0.012) ...
];

doeTable = struct2table(rows, 'AsArray', true);
doeTable = doeTable(:, cfg.doeFieldOrder);
writetable(doeTable, cfg.doeManifestCsv);
end

function row = make_row(pointId, a1, a2, b2, r0)
row = struct( ...
    'point_id', string(pointId), ...
    'a1', a1, ...
    'a2', a2, ...
    'b2', b2, ...
    'r0', r0 ...
);
end
